/* Bir web sayfasında bulunan bütün elementlerin toplamına DOM denir.

DOM Nedir?
DOM (Document Object Module) HTML dökümanları içerisinde bulunan nesnelere 
kolaylıkla erişim sağlamak ve üzerinde işlemler yapabilmek için tasarlanan 
bir modeldir. DOM ile HTML dosyamızda bulunan her şeye erişim sağlayabiliriz.

HTML dosyasında bulunan bütün öğeler, DOM üzerinde bir nesne olarak görülür.
DOM modelin ismi, document ise bu nesnelerin kapsayıcısıdır. Bu kapsayıcı içerisinde 
bulunan <html> evet bildiğiniz html etiketi head, body nesnelerinin içerisinde bulunan 
diğer nesnelerin kapsayıcısıdır. Head ve body etiketleri de içerisinde bulunan diğer 
nesnelerin kapsayıcılarıdır. Aynı örüntü üzerinde bu durum tekrar eder.

DOM'u kısaca birbirleri içerisinden çıkan matruşka bebeklere benzetebiliriz. Document objesi
ise bu bebeklerin en büyüğü olarak düşünülebilir.
*/
