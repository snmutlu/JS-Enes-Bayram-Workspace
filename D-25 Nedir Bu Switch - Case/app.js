/*

Switch - Case yapısı if-else yapısıyla neredeyse aynıdır.
if-else yapısına alternatif gibi düşünebiliriz.
Bir ihtimal ve yol ayrımı olan yerlerde if-else'ler gibi switch-case'leri kullanabiliz.

Örn;

switch(değer){

    case1:
    !kodlar
    break;

    case1:
    !kodlar
    break;

    case1:
    !kodlar
    break;

    default:
    !kodlar
    break;
}

*Örnekteki yapıda görüldüğü üzere switch değeri hangi case değerine eşitse o case
*içerisindeki kodlar çalıştırılıyor. Eğer hiçbir case değeri switch değerine
*eşit değilse default içerisindeki kodlar çalıştırılıyor.
















*/